/*
 * Multithreading is a Java feature that allows concurrent execution of two or
 * more parts of a program for maximum utilization of CPU.
 * Each part of such program is called a thread.
 * So, threads are light-weight processes within a process.
 * 
 * we use multithreading when we want to perform multitasking within a process.
 * 
 * multiprocessing is performing multiple task at a same time.
 * 
 * multithreading helps in performming concurrency and not parallelism
 * 
 * concurrency is running and managing multiple tasks at a same time while
 * parallelism is actually doing multiple tasks simultaneously
 * 
 * multithreading can be performed in two ways
 * 
 * 1. --> Extending the thread class
 * 2. --> Implementing the runnable interface
 * 
 */